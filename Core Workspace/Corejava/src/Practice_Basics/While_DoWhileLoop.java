package Practice_Basics;

public class While_DoWhileLoop {

	public static void main(String[] args) {

		int i = 20;

		while (i < 20) {
			System.out.println(i);
			i++;
		}

		System.out.println();

		// Do While Loop Test.
		System.out.println("Do While Loop");

		int a = 20;

		do {
			System.out.println(a);
			a++;
		} while (a < 20);

	}

}
