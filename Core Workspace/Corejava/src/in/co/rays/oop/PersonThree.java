package in.co.rays.oop;

import java.util.Date;

public class PersonThree {
	
	private int id;
	
	private String name;
	private Date dob;
	
	
	
	public void setId(int id) {
		this.id=id;	
	}
	public int getId() {
		return id;	
	}
	
	public void setName(String name) {
		this.name=name;
	}
	public String getName() {
		return name;	
	}
	
	public void setDob(Date dob) {
		this.dob=dob;	
	}
	public Date getDob() {
		return dob;
		}
	
}
