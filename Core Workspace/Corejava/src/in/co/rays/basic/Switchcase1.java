package in.co.rays.basic;

public class Switchcase1 {
	
	public static void main(String[] args) {
		
		String month="march";
		
		
		switch (month) {
		case "january":
			System.out.println("this is first month of year");
			break;
			
		case "fabuary":
			
			System.out.println("this is second month of year");
			


		}
		
		
	}

}
