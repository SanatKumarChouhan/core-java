package Practice;

public class SwitchCase2 {
	
	public static void main(String[] args) {
		
		int RollNo=12;
		
		switch (RollNo) {
		case 10:
			System.out.println("this is riya's roll number");
			break;
		
		case 11:
			System.out.println("this is prakhar's roll number");
			break;
		
		case 12:
			System.out.println("this is sameer's roll number");
			break;
			
		default:
			System.out.println("roll number not found");
		}
	}
	
	

}
