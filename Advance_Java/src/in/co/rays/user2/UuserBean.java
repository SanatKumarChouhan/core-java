package in.co.rays.user2;

import java.util.Date;

public class UuserBean {
	
	private int id;
	private String naam;
	private String upnaam;
	private String lgid;
	private String pass;
	private Date din;
	private String pata;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNaam() {
		return naam;
	}
	public void setNaam(String naam) {
		this.naam = naam;
	}
	public String getUpnaam() {
		return upnaam;
	}
	public void setUpnaam(String upnaam) {
		this.upnaam = upnaam;
	}
	public String getLgid() {
		return lgid;
	}
	public void setLgid(String lgid) {
		this.lgid = lgid;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public Date getDin() {
		return din;
	}
	public void setDin(Date din) {
		this.din = din;
	}
	public String getPata() {
		return pata;
	}
	public void setPata(String pata) {
		this.pata = pata;
	}
	
	
	
}
