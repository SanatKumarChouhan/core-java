package Practice;

public class HShape {
	
	private String color;
	private int borderwidth;
	
	
	public void setColor(String color) {
		this.color=color;
	}
	public String getColor() {
		return color;
	}
	
	
	public void BorderWidth(int borderwidth) {
		this.borderwidth=borderwidth;
	}
	public int BorderWidth() {
		return borderwidth;
	}
	

}
